/******************************************************************************
* \Syntax          : void Relay_SetValue(Uint_8 Pin_num, Relay_Value_t Relay_Value)
* \Description     : Relay SetValue
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : parameterName   Parameter Describtion
* \Parameters (out): None
* \Return value:   : Std_ReturnType  E_OK
*                                    E_NOT_OK
*******************************************************************************/
void Relay_SetValue(Uint_8 Pin_num, Relay_Value_t Relay_Value)
{
	DIO_WriteChannel(Pin_num,Relay_Value);
}

/******************************************************************************
* \Syntax          : void Relay_Toggle(Uint_8 Pin_num)
* \Description     : Relay1 TOGGLE
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : parameterName   Parameter Describtion
* \Parameters (out): None
* \Return value:   : Std_ReturnType  E_OK
*                                    E_NOT_OK
*******************************************************************************/
void Relay_Toggle(Uint_8 Pin_num)
{
	DIO_FlipChannel(Pin_num);
}


