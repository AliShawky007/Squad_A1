/******************************************************************************
* \Syntax          : uint16 TempSensor_Read(void)
* \Description     : Describe this service
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : parameterName   Parameter Describtion
* \Parameters (out): None
* \Return value:   : uint16  E_OK
*                                    E_NOT_OK
*******************************************************************************/
uint16 TempSensor_Read(void)
{
	uint8 i =0 , j =0 ;
	uint32 temp = 0;
	static uint16 Temprature = 0;
	ADC_Read();
	for (i = 0 ; i < size_arr ; i++)
	{
		arr[i]=ADC_Vin_Value;
	}
	for(i=0;i<size_arr-1;i++)
	{
		for(j=i+1;j<size_arr;j++)
		{
			if(arr[j]<arr[i])
			{
				temp=arr[j];
				arr[j]=arr[i];
				arr[i]=temp;
			}
		}
	}
	Temprature =arr[size_arr/2]/10;
	return Temprature ;
}

