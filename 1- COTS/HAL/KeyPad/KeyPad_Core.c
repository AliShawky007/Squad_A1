/******************************************************************************
* \Syntax          : Std_ReturnType FunctionName(AnyType parameterName)        
* \Description     : Describe this service                                    
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Non Reentrant                                             
* \Parameters (in) : parameterName   Parameter Describtion                     
* \Parameters (out): None                                                      
* \Return value:   : Std_ReturnType  E_OK
*                                    E_NOT_OK                                  
*******************************************************************************/
void KEYPAD_Init(void)
{
	DIO_WriteChennel(KEYPAD_ROW0,PIN_HIGH);
	DIO_WriteChennel(KEYPAD_ROW1,PIN_HIGH);
	DIO_WriteChennel(KEYPAD_ROW2,PIN_HIGH);
	DIO_WriteChennel(KEYPAD_ROW3,PIN_HIGH);	
}

/******************************************************************************
* \Syntax          : uint8 KEYPAD_GetValule(void)
* \Description     : return the value of button pressed
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : parameterName   Parameter Describtion
* \Parameters (out): None
* \Return value:   : void  E_OK
*                                    E_NOT_OK
*******************************************************************************/
uint8 KEYPAD_GetValule(void)
{	
	uint8 ROW_POS=0 , COL_POS=0;
	uint8 Button_Val=0, Temp=0;
	for (ROW_POS=ROW_INIT;ROW_POS<=ROW_FINAL;ROW_POS++)
	{
		DIO_WriteChennel(ROW_POS,PIN_LOW);
		for (COL_POS=COL_INIT;COL_POS<=COL_FINAL;COL_POS++)
		{
			DIO_ReadChennel(COL_POS,&Temp);
			if (Temp == 0)
			{
				Button_Val = KEYPAD_Val[ROW_POS-ROW_INIT][COL_POS-COL_INIT];
				while (Temp == 0)
				{
					DIO_ReadChennel(COL_POS,&Temp);
				}
				_delay_ms(10);
			}
		}
		DIO_WriteChennel(ROW_POS,PIN_HIGH);
	}
	return Button_Val;
}

